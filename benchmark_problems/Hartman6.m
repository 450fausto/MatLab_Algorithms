function f=Hartman6(x)
% Hartman 6 Problem (H6) n=6
% The number of local minima is four
% LimInf=[0 0 0 0 0 0]; LimSup=[1 1 1 1 1 1];
% Fojmin?-3.322368
% x*?[0.201690,0.150011,0.476874,0.275332,0.311652,0.657301]
a=[10 3 17 3.5 1.7 8; 0.05 10 17 0.1 8 14; 3 3.5 1.7 10 17 8;...
    17 8 0.05 10 0.1 14];
p=[ 0.1312 0.1696 0.5569 0.0124 0.8283 0.5886;...
    0.2329 0.4135 0.8307 0.3736 0.1004 0.9991;...
    0.2348 0.1451 0.3522 0.2883 0.3047 0.665;...
    0.4047 0.8828 0.8732 0.5743 0.1091 0.0381];
c=[1 1.2 3 3.2];
for i=1:4
    for j=1:6
        f1(j)=a(i,j)*(x(j)-p(i,j))^2;
    end
    s1=-sum(f1);
    f2(i)=c(i)*exp(s1);
end
f=-sum(f2);
end
