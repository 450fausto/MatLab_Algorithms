function f=Shubert(x)
% Shubert Problem (SBT)  n=2
% LimInf=[-10 -10 ... -10]; LimSup=[10 10 ... 10];
% Fojmin~=-186.7309 
% x*=[-7.0835,4.8580],[-7.0835,-7.7083],(-1.4251,-7.0835),(5.4828,4.8580),
% (-1.4251,-0.8003),(4.8580,5.4828),(-7.7083,-7.0835),(-7.0835,-1.4251),
% (-7.7083,-0.8003),(-7.7083,5.4828),(-0.8003,-7.7083),(-0.8003,-1.4251),
% (-0.8003,4.8580),(-1.4251,5.4828),(5.4828,-7.7083),(4.8580,-7.0835),
% (5.4828,-1.4251),(4.8580,-0.8003)
n=length(x);
for i=1:n
    for j=1:5
        S(j)=j*cos((j+1)*x(i)+j);
    end
    P(i)=sum(S);
end
f=prod(P);
end
