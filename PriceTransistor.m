function f=PriceTransistor(x)
% Price’s Transistor Modelling Problem (PTM) n=9
% LimInf=[-10 -10 ... -10]; LimSup=[10 10 ... 10];
% Fojmin=0
% x*=[0.9 0.45 1 2 8 8 5 1 2]
gamma=x(1)*x(3)-x(2)*x(4);
g=[0.4850 0.7520 0.8690 0.9820; 0.3690 1.2540 0.7030 1.4550; 5.2095...
   10.0677 22.9274 20.2153; 23.3037 101.7790 111.4610 191.2670; 28.5132...
   111.8467 134.3884 211.4823];
for k=1:4
    alfa(k)=(1-x(1)*x(2))*x(3)*(exp(x(5)*(g(1,k)-g(3,k)*x(7)/1000-...
        g(5,k)*x(8)/1000))-1)-g(5,k)+g(4,k)*x(2);
    beta(k)=(1-x(1)*x(2))*x(4)*(exp(x(6)*(g(1,k)-g(2,k)-...
        g(3,k)*x(7)/1000+g(4,k)*x(9)/1000))-1)-g(5,k)*x(1)+g(4,k);
    S(k)=(alfa(k)^2+beta(k)^2);
end
f=gamma^2+sum(S);